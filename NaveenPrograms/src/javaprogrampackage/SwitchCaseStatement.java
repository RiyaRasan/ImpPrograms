public class SwitchCaseStatement {

	public static void main(String[] args) {
		
		int value=1;
		
		switch (value) {
		case 'a':
			System.out.println("This is a");
			break;
			
		case 'b':
			System.out.println("This is a");
			break;
			
		case 'c':
			System.out.println("This is a");
			break;
			
		case 'd':
			System.out.println("This is a");
			break;

		default:
			break;
		}

	}

}
